#!/bin/bash

# Generate TOOLS.md — a Terminal Trove-style catalog of every tool this repo
# installs, built straight from the Brewfile.* bundles (the single source of
# truth). Each Brewfile is `brew bundle dump --describe` output, so the one-line
# description already lives above each entry; this script just reshapes it into
# grouped Markdown tables. No network, no `brew`, no hand-maintained list.
#
# Single source of truth shared by CI (.github/workflows/ci.yml) and the
# lefthook pre-commit hook, mirroring bin/check_brewfile_sort.sh.
#
# Usage:
#   ./bin/gen_tools_list.sh            Regenerate TOOLS.md in place.
#   ./bin/gen_tools_list.sh --check    Fail (exit 1) if TOOLS.md is out of sync.
#   ./bin/gen_tools_list.sh --stdout   Print the rendered Markdown to stdout.
# Extra positional args override the input Brewfiles (used by the bats tests).

# This script emits literal Markdown, so its printf format strings contain
# backticks (code spans) that are deliberately NOT command substitution. All
# real expansion here goes through $(...) / ${...} in double quotes.
# shellcheck disable=SC2016

set -euo pipefail

BASE_DIR=$(cd "$(dirname "$(readlink -f "$0")")/.." || exit 1; pwd)
cd "$BASE_DIR" || exit 1

# TOOLS_OUTPUT lets the bats tests redirect the target file to a temp path.
OUTPUT="${TOOLS_OUTPUT:-TOOLS.md}"

mode="write"
files=()
while [ "$#" -gt 0 ]; do
    case "$1" in
        --check) mode="check" ;;
        --stdout) mode="stdout" ;;
        *) files+=("$1") ;;
    esac
    shift
done
if [ "${#files[@]}" -eq 0 ]; then
    files=(Brewfile.basic Brewfile.common Brewfile.macos Brewfile.linux)
fi

# Human-friendly heading and blurb per known Brewfile layer. Unknown fixture
# names fall back to the suffix and no blurb.
layer_title() {
    case "$(basename "$1")" in
        Brewfile.basic) echo "Basic — shell startup layer" ;;
        Brewfile.common) echo "Common — full workstation toolkit" ;;
        Brewfile.macos) echo "macOS apps" ;;
        Brewfile.linux) echo "Linux" ;;
        Brewfile.*) echo "${1##*Brewfile.}" ;;
        *) basename "$1" ;;
    esac
}
layer_blurb() {
    case "$(basename "$1")" in
        Brewfile.basic) echo "The minimal layer the shell depends on at load time; CI installs only this bundle." ;;
        Brewfile.common) echo "Full-workstation tooling not required to start the shell." ;;
        Brewfile.macos) echo "Platform-specific desktop apps (Homebrew casks)." ;;
        Brewfile.linux) echo "Linux-specific additions." ;;
        *) echo "" ;;
    esac
}

# Escape the Markdown table-cell separator so descriptions never break a row.
escape_cell() {
    printf '%s' "${1//|/\\|}"
}

# Emit the table rows for one Brewfile on stdout; nothing if it has no
# brew/cask entries. Callers count the emitted rows themselves — this runs in a
# command-substitution subshell, so it cannot report a count back via a global.
render_rows() {
    local bf="$1"
    local pending="" line kind name desc linked
    while IFS= read -r line || [ -n "$line" ]; do
        if [[ "$line" =~ ^#[[:space:]]?(.*)$ ]]; then
            pending="${BASH_REMATCH[1]}"
            continue
        fi
        if [[ "$line" =~ ^(brew|cask|tap)[[:space:]]\"([^\"]+)\" ]]; then
            kind="${BASH_REMATCH[1]}"
            name="${BASH_REMATCH[2]}"
            # Taps are plumbing, not tools — drop them (and their comment).
            if [ "$kind" = "tap" ]; then
                pending=""
                continue
            fi
            desc="$pending"
            pending=""
            # Only core-tap names (no slash) resolve on formulae.brew.sh.
            if [[ "$name" == */* ]]; then
                linked="\`$name\`"
            elif [ "$kind" = "cask" ]; then
                linked="[$name](https://formulae.brew.sh/cask/$name)"
            else
                linked="[$name](https://formulae.brew.sh/formula/$name)"
            fi
            printf '| %s | %s |\n' "$linked" "$(escape_cell "$desc")"
        else
            # Blank/other line: drop any dangling comment so it can't attach to
            # a later entry it was never meant to describe.
            [ -z "${line//[[:space:]]/}" ] && pending=""
        fi
    done <"$bf"
}

render_all() {
    local total=0 bundles=0 bf rows
    printf '# Tools\n\n'
    printf '<!-- Generated by bin/gen_tools_list.sh from Brewfile.*. Do not edit by hand. -->\n'
    printf '<!-- Regenerate: ./bin/gen_tools_list.sh — CI and lefthook enforce that it stays in sync. -->\n\n'
    printf 'Every CLI tool and app this repo installs via Homebrew, catalogued straight from the\n'
    printf '`Brewfile.*` bundles. Descriptions are Homebrew'\''s own (`brew bundle dump --describe`),\n'
    printf 'so this page never drifts from what actually gets installed. Grouped by bundle layer.\n\n'

    local count blurb plural
    for bf in "${files[@]}"; do
        [ -f "$bf" ] || continue
        rows=$(render_rows "$bf")
        [ -z "$rows" ] && continue
        count=$(printf '%s\n' "$rows" | grep -c '^|')
        total=$((total + count))
        bundles=$((bundles + 1))
        plural="tools"
        [ "$count" -eq 1 ] && plural="tool"
        printf '## %s\n\n' "$(layer_title "$bf")"
        blurb=$(layer_blurb "$bf")
        [ -n "$blurb" ] && printf '%s\n\n' "$blurb"
        printf '_`%s` — %d %s._\n\n' "$(basename "$bf")" "$count" "$plural"
        printf '| Tool | Description |\n| --- | --- |\n'
        printf '%s\n\n' "$rows"
    done

    printf -- '---\n\n'
    printf '_%d tools across %d bundles._\n' "$total" "$bundles"
}

case "$mode" in
    stdout)
        render_all
        ;;
    write)
        render_all >"$OUTPUT"
        echo "Wrote $OUTPUT"
        ;;
    check)
        if ! diff -u "$OUTPUT" <(render_all) >/dev/null 2>&1; then
            echo "x $OUTPUT is out of sync with the Brewfiles." >&2
            echo "  Run ./bin/gen_tools_list.sh and commit the result." >&2
            exit 1
        fi
        ;;
esac
